        <div class="row abt po">
            The Department of Computer science and Engineering (CSE) was established in the year 1984 to meet the needs of the emerging IT industry and research. The department started with an initial intake of 60 students in UG programme in CSE and enhanced its intake to 120 in the year 1994. In 1998, the department started a PG programme in “Computer Science and Engineering (CSE)” with an intake of 18 students. Presently the department has two PG programs in the branch of Computer Science and Engineering and Computer Network Engineering with in-take of 30 and 18 students respectively.         
            <br><b><h3>   VISION :</h3></b>
            To build a strong learning and research environment in the field of Computer Science and Engineering that responds to the challenges of 21st century.
            <br><b><h3>   MISSION :</h3></b>
            <ul>
                <li>To produce computer science graduates who, trained in design and implementation of computational systems through competitive curriculum and research in collaboration with industry and other organizations.
                <li>To educate students in technology competencies by providing professionally committed faculty and staff.
                <li>To inculcate strong ethical values, leadership abilities and research capabilities in the minds of students so as to work towards the progress of the society.</li>
            </ul>
