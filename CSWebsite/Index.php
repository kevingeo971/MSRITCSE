            <div class="col-lg-8 ht"><b>PROGRAMME OUTCOMES (POs)</b></div>
        </div>
        <div class="row po">
            <br>The outcomes of the Bachelor of Engineering in Computer Science & Engineering Programme are as follows: 
            <br>A B.E. (Computer Science & Engineering) graduate must demonstrate :          
            <ol>
                <li>An ability to apply knowledge of mathematics, science, and engineering as it applies to Computer Science & Engineering</li>
                <li>An ability to identify, formulate, study, and analyze and solve complex computing problems</li>
                <li>An ability to design a computer-based system, component, software or process to meet the desired needs.</li>
                <li>An ability to design and conduct experiments, evaluate results and provide valid conclusions.</li>
                <li>An ability to use modern computing techniques, technologies and tools necessary for computing engineering practice.</li>
                <li>An ability to understand and assess the societal, legal and security issues related to the practice of computer science and engineering.</li>
                <li>An ability to understand the impact of computing solutions in an economic, environmental and societal context.</li>
                <li>An understanding of professional and ethical responsibilities in professional engineering practice</li>
                <li>An ability to function effectively individually and in team, and in multi-disciplinary environment.</li>
                <li>An ability to communicate effectively.</li>
                <li>An understanding of the engineering and management principles required for project and finance management.</li>
                <li>Recognition of the need for, and an ability to engage in life-long learning.</li>
